  <h2 align="left">Covid-19</h2>
  <p align="left">
    Covid-19 atau biasa kita sebut corona virus adalah penyakit menular yang disebabkan oleh virus SARS-CoV-2.
    Virus ini yang menyebabkan pandemi coronavirus 2019-2020. Penderita coronavirus dapat mengalami demam, batuk-batuk,
    dan sesak nafas. Dalam beberapa kasus, sakit tenggorokan, pilek, atau bersin-bersin juga dialami tetapi cukup jarang.
    Infeksi menyebar dari satu orang ke orang lain melalui percikan dari saluran pernapasan yang sering dihasilkan saat batuk atau bersin.
    Waktu bertahan coronavirus sekitar 1-14 hari dengan rata-rata 5 hari. Untuk menghindari penyakit ini, mencuci tangan setelah bepergian,
    menjaga jarak dengan orang yang batuk atau bersin, dan tidak menyentuh wajah sebelum mencuci tangan dengan sabun sangat dianjurkan.
